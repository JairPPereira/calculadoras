	<h1><i class="icon-custom icon-lg rounded-2x icon-color-u fa fa-question"></i>Como funciona a regra de três simples em proporcionalidade direta?</h1>
	<h4>pequenas respostas para grandes perguntas</h4>
</div>

<div class="tag-box tag-box-v1 margin-bottom-20">
	<p><span class="dropcap">A</span> regra de três simples é um processo muito prático para resolver problemas que envolvem quatro números, dos quais se conhecem três. Dito de outra forma, queremos encontrar um valor a partir de outros três.</p>
	<p>Mas antes de explicar o funcionamento da regra é importante salientar que ela só pode ser utilizada se os números envolvidos forem <b>diretamente</b> ou <b>inversamente</b> proporcionais. Um pequeno exemplo ajudará a perceber a diferença entre estes dois conceitos.</p>
	<p>Temos o seguinte problema: Uma bicicleta percorre 80 km em 2 horas, mantendo o mesmo ritmo, em quanto tempo ela percorrerá 20 km? Vamos começar por construir uma tabela e designar por `x` o valor que queremos descobrir:</p>
	<table class="table table-striped" style="width: 60%">
		<thead>
			<tr><th style="text-align:center">Percurso (km)</th><th style="text-align:center">Tempo (h)</th></tr>
		</thead>
		<tbody>				
			<tr><td style="text-align:center">80</td><td style="text-align:center">2</td></tr>
			<tr><td style="text-align:center">20</td><td style="text-align:center">`x`</td></tr>
		</tbody>		
	</table>
	<img class="img-responsive" style="margin:10px auto;" src="images/faq/bicicleta.png" alt="bicicleta" />
	<p>E agora pensemos, se o percurso diminuir o que vai acontecer ao tempo? Como a bicicleta vai percorrer uma distância menor, vai precisar de menos tempo, logo este vai diminuir também. Sempre que ambas as grandezas diminuírem ou ambas aumentarem estamos na presença de <b>proporcionalidade direta</b>.</p>
	<p>Temos então a seguinte relação: `80/20 = 2/x`</p>
	<p>Para encontrar o valor pretendido, basta agora fazer a multiplicação cruzada e resolver a equação:</p>
	<p>`80 xx x = 20 xx 2 hArr x = (20 xx 2)/80 hArr x = 40/80 hArr x = 0.5`</p>
	<p>A bicicleta irá demorar 0,5 horas, ou seja, 30 minutos a percorrer os 20 km.</p>
		

